#!/usr/bin/env node
/// deploy-evm-cadence.mjs — Deploy EVM contracts via Cadence COA (no gateway required).
///
/// This script replaces `npm run deploy-evm` (which requires `flow evm gateway`).
/// It uses the Cadence transaction `deploy_evm_contracts.cdc` to deploy
/// MockToken and PrizePool from the COA — no external EVM private key signing needed.
///
/// Prerequisites:
///   - Flow emulator running: flow emulator
///   - Cadence contracts deployed: bash scripts/deploy-cadence.sh
///   - Solidity contracts compiled: npm run compile
///
/// Usage:
///   npm run deploy-evm-cadence

import { AbiCoder, parseEther } from "ethers"
import { readFileSync, writeFileSync } from "fs"
import { spawnSync } from "child_process"
import { dirname, join } from "path"
import { fileURLToPath } from "url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const projectRoot = join(__dirname, "..")

function flowTx(txFile, cadenceArgs, extraFlags = []) {
  // Use --args-json for structured Cadence argument passing
  const argsJson = JSON.stringify(cadenceArgs)
  const result = spawnSync(
    "flow",
    ["transactions", "send", txFile, "--args-json", argsJson, ...extraFlags],
    { cwd: projectRoot, encoding: "utf8", maxBuffer: 10 * 1024 * 1024 }
  )
  if (result.error) throw result.error
  return result.stdout + result.stderr
}

// ── Load compiled artifacts ────────────────────────────────────────────────
const mockTokenArtifact = JSON.parse(
  readFileSync(join(projectRoot, "artifacts/contracts/MockToken.sol/MockToken.json"), "utf8")
)
const prizePoolArtifact = JSON.parse(
  readFileSync(join(projectRoot, "artifacts/contracts/PrizePool.sol/PrizePool.json"), "utf8")
)

// ── ABI-encode MockToken constructor args: (string name, string symbol, uint256 supply) ──
const abiCoder = new AbiCoder()
const mockTokenCtorArgs = abiCoder.encode(
  ["string", "string", "uint256"],
  ["Prize Token", "PTK", parseEther("1000000")]   // 1M PTK initial supply
)
// Full creation bytecode = contract bytecode + ABI-encoded constructor args
const mockTokenFullHex = mockTokenArtifact.bytecode.slice(2) + mockTokenCtorArgs.slice(2)
const prizePoolHex = prizePoolArtifact.bytecode.slice(2)  // ctor arg added in Cadence tx

console.log("=== Prize Pool — EVM Deploy via COA ===")
console.log("MockToken bytecode:", mockTokenFullHex.length / 2, "bytes")
console.log("PrizePool bytecode:", prizePoolHex.length / 2, "bytes")
console.log("")

// ── Step 1: Deploy contracts via Cadence COA ─────────────────────────────
console.log("[1/2] Deploying MockToken + PrizePool via COA...")
const deployOutput = flowTx(
  "cadence/transactions/deploy_evm_contracts.cdc",
  [
    { type: "String", value: mockTokenFullHex },
    { type: "String", value: prizePoolHex },
  ],
  ["--signer", "emulator-account", "--network", "emulator", "--include", "all"]
)

// Parse contract addresses from EVM.TransactionExecuted events
const contractAddressMatches = [
  ...deployOutput.matchAll(/contractAddress \(String\): "([^"]+)"/g),
]
if (contractAddressMatches.length < 2) {
  console.error("Failed to parse deployed addresses from output")
  console.error(deployOutput)
  process.exit(1)
}
const mockTokenAddress = contractAddressMatches[0][1]
const prizePoolAddress = contractAddressMatches[1][1]

console.log("  MockToken deployed:", mockTokenAddress)
console.log("  PrizePool deployed:", prizePoolAddress)
console.log("  (COA is owner — no transferOwnership needed)")
console.log("")

// ── Step 2: Register PrizePool address in PrizePoolOrchestrator ───────────
console.log("[2/2] Registering PrizePool address in PrizePoolOrchestrator...")
flowTx(
  "cadence/transactions/set_prize_pool_address.cdc",
  [{ type: "String", value: prizePoolAddress }],
  ["--signer", "emulator-account", "--network", "emulator"]
)
console.log("  Done.")
console.log("")

// ── Write client/.env ────────────────────────────────────────────────────
const clientEnvPath = join(projectRoot, "client", ".env")
writeFileSync(
  clientEnvPath,
  [
    `# Auto-generated by deploy-evm-cadence.mjs`,
    `VITE_TOKEN_ADDRESS=${mockTokenAddress}`,
    `VITE_PRIZE_POOL_ADDRESS=${prizePoolAddress}`,
    "",
  ].join("\n")
)
console.log(`  Wrote ${clientEnvPath}`)
console.log("")

// ── Summary ───────────────────────────────────────────────────────────────
console.log("─────────────────────────────────────────────")
console.log("Deployment complete!")
console.log("  MockToken address:", mockTokenAddress)
console.log("  PrizePool address:", prizePoolAddress)
console.log("")
console.log("Then: cd client && npm install && npm run dev")
